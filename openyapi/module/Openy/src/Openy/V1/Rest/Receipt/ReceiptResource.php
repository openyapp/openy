     * @param  mixed $id
     * @return ApiProblem|mixed
     */
    public function fetch($id)
    {
        $receipt = $this->mapper->fetch($id);
        if ($receipt == FALSE || $receipt->receiptid == FALSE)
            return new ApiProblem(404, 'Receipt not found');
        return $receipt;

    }

    /**
     * Fetch all or a subset of resources
     *
     * @param  array $params
     * @return ApiProblem|mixed
     */
    public function fetchAll($params = array())
    {
        if (isset($params['until'])){
            $filter = new \Zend\Filter\DateTimeFormatter();
            $filter->setFormat('Ymd');
            try{
                $params['until']=$filter->filter($params['until']);
            }catch(\Exception $e){
                return new ApiProblem(400, 'Invalid value for parameter "until"');
            }
        }
        return $this->mapper->fetchAll($params);
    }

